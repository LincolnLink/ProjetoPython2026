"""
Python = Lingagem de programação
Tipo de tipagem = dinamica / force
srt -> string -> texto
Strings são textos que estão dentro de aspas
"""

print(1234)

# Aspas simples
print('Olá, Mundo!')

# Aspas duplas
print("Olá, Mundo!")

# Escape
print("Olá, \nMundo!")  # Quebra de linha

print("Olá, \tMundo!")  # Tabulação

print("Olá, \"Mundo\"!")  # Aspas duplas dentro de aspas duplas

print('Olá, \'Mundo\'!')  # Aspas simples dentro de aspas simples

print("C:\\Users\\João")  # Barra invertida,
# nem se usa
print(r"Lincoln \"Ferreira\"")  # Raw string - não interpreta escape

# como se usa!
print(1, "Ola 'Lincoln'")